class ClassOfThousandLines:
    def main(self):
        self._do_one()
        self._do_three()
        self._do_two()

    def _do_one(self):
        print("d1")
        print("d11")
        print("d12")

    def _do_two(self):
        print("d2")
        print("d21")
        print("d22")

    def _do_three(self):
        print("d3")
        print("d31")
        print("d32")
